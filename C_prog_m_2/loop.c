#include <stdio.h>
int main()
{

  /*"Write a C program to print numbers from 1 to 18 using all three types of Loops
   (while, for, do-while")*/
  int i = 1, j;
  printf("by using while loop");
  while (i <= 10)
  {
    printf("\n\t%d", i);
    i++;
  }
  printf("\n by using for loop");
  for (j = 0; j < 10; j++)
  {
    printf("\n\t%d", j);
  }
  int k = 1;
  printf("\nby using do while loop");
  do
  {
    printf("\n\t%d", k);
    k++;
  } while (kc = 10);
  printf("\n");
}